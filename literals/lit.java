package literals;
class lit {

      public static void main(String a[]){

            // Different types of literals in Java

            // Integer literals
            int decimalLiteral = 100; // Decimal literal
            int hexLiteral = 0x55; // Hexadecimal literal
            int octalLiteral = 0144; // Octal literal
            int binaryLiteral = 0b1100100; // Binary literal

            // Floating point literals
            float floatLiteral = 10.5f; // Float literal
            double doubleLiteral = 10.99; // Double literal

            // Character literal
            char charLiteral = 'A'; // Character literal

            // String literal
            String stringLiteral = "Hello, Java!"; // String literal

            // Boolean literals
            boolean trueLiteral = true; // Boolean true literal
            boolean falseLiteral = false; // Boolean false literal

            // Print all literals
            System.out.println("Integer Literals:");
            System.out.println("Decimal: " + decimalLiteral);
            System.out.println("Hexadecimal: " + hexLiteral);
            System.out.println("Octal: " + octalLiteral);
            System.out.println("Binary: " + binaryLiteral);
            System.out.println("#############################");
            System.out.println("\nFloating Point Literals:");
            System.out.println("Float: " + floatLiteral);
            System.out.println("Double: " + doubleLiteral);
            System.out.println("#############################");
            System.out.println("\nCharacter Literal:");
            System.out.println("Character: " + charLiteral);
            System.out.println("#############################");
            System.out.println("\nString Literal:");
            System.out.println("String: " + stringLiteral);
            System.out.println("#############################");
            System.out.println("\nBoolean Literals:");
            System.out.println("True: " + trueLiteral);
            System.out.println("False: " + falseLiteral);

      }

}
